/**
 * @extends ServiceEntityRepository<Menu>
 */
class MenuRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Menu::class);
    }

    public function findOrderedMenuItems(string $menuGroup)
    {
        return $this->createQueryBuilder('m')
            ->where('m.menugroup = :group')
            ->andWhere('m.menuparent IS NULL')
            ->setParameter('group', $menuGroup)
            ->orderBy('m.menuorder', 'ASC') // Ensure you have the correct field here
            ->leftJoin('m.menuchildren', 'c') // Join the children
            ->addSelect('c')
            ->orderBy('c.menuorder', 'ASC') // Sort children
            ->getQuery()
            ->getResult();
    }

